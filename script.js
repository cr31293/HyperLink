$('#normal').on('click', '.button', function (event) {
    event.preventDefault();
    console.log($(this).attr('data-id'));
    var id = $(this).attr('data-id');
    buildTable(id, labels);
});


function buildTable(id, labels) {
    var table = document.createElement('table');
    var thead = document.createElement('thead');
    var tbody = document.createElement('tbody');
    var container = document.getElementById('table-container');
    var newImg = document.createElement('img');
    newImg.setAttribute('src', attacks[id].image);
    

    var theadTr = document.createElement('tr');
    
    for(var prop in attacks[id].stats[0]) {
        var theadTh = document.createElement('th');
        theadTh.innerHTML = labels[prop];
        theadTr.appendChild(theadTh);
    }


    thead.appendChild(theadTr);
    table.appendChild(thead);
    attacks[id].stats.forEach(function(attack) {
        var tbodyTr = document.createElement('tr');
        for (var prop in attack) {
            console.log(attack[prop]);
            var tbodyTd = document.createElement('td');
            tbodyTd.innerHTML = attack[prop];
            tbodyTr.appendChild(tbodyTd);
        }
        tbody.appendChild(tbodyTr);    
    })

    // for (var a = 0; a < data.length; a++) {
    
    // }
    table.appendChild(tbody);

    
    container.innerHTML = "";
    var h1 = document.createElement('h1');
    h1.textContent = attacks[id].name;
    container.append(h1);
    container.append(newImg);
    container.append(table);
}

